    public void addTranslation(String key, String language, String translation) {
        Map<String, String> langMap = translations.get(language);
        if (langMap != null) {
            langMap.put(key, translation);
        }
    }

    public static String getTranslation(String key, String language) {
        if (translations == null) {
            return key;
        }

        Map<String, String> langMap = translations.get(language);
        if (langMap != null && langMap.containsKey(key)) {
            return langMap.get(key);
        }

        Map<String, String> englishMap = translations.get("en");
        if (englishMap != null && englishMap.containsKey(key)) {
            return englishMap.get(key);
        }

        return key;
    }

    public String getTranslation(String key, String language, Map<String, String> placeholders) {
        String translation = getTranslation(key, language);

        if (placeholders != null) {
            for (Map.Entry<String, String> entry : placeholders.entrySet()) {
                translation = translation.replace("{" + entry.getKey() + "}", entry.getValue());
            }
        }

        return translation;
    }

    public static boolean isLanguageSupported(String language) {
        return translations != null && translations.containsKey(language);
    }

    public static String get(String key, String language) {
        return getTranslation(key, language);
    }

    public String[] getSupportedLanguages() {
        if (translations == null) {
            return new String[0];
        }
        return translations.keySet().toArray(new String[0]);
    }
